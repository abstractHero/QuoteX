package com.phantasmdragon.quote.dataLayer.database.dao

import android.arch.lifecycle.LiveData
import android.arch.persistence.room.Dao
import android.arch.persistence.room.Delete
import android.arch.persistence.room.Insert
import android.arch.persistence.room.OnConflictStrategy.REPLACE
import android.arch.persistence.room.Query
import com.phantasmdragon.quote.dataLayer.database.entity.AuthorEntity

@Dao
interface AuthorDao {

    @Query("SELECT * FROM Authors WHERE id = :id")
    fun getAuthorById(id: Int): LiveData<AuthorEntity>

    @Insert(onConflict = REPLACE)
    fun insert(authorEntity: AuthorEntity): Long

    @Delete
    fun delete(authorEntity: AuthorEntity)

}